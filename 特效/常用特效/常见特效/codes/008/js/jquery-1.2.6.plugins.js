/*
 * jquery.elementUtil
 * Copyright (c) 2008 Naoki Ueno
 * Dual licensed under the MIT (http://www.opensource.org/licenses/mit-license.php)
 * and GPL (http://www.opensource.org/licenses/gpl-license.php) licenses.
 * Date: 1/5/2009
 *
 * @id jQuery.fn.hoverElement
 *
 * @return {jQuery} Returns the same jQuery object, for chaining.
 *
 * @example jQuery('div.followingNav li').hoverElement();
 *
 * @example jQuery('div.followingNav li').hoverElement({ hoverClass : 'followingPageListB_o' });
 *
 * @id jQuery.fn.stayElement
 *
 * @return {jQuery} Returns the same jQuery object, for chaining.
 *
 * @example jQuery('div.followingNav li').stayElement({stayObject});
 *
 * @example jQuery('div.followingNav li').hoverElement({ hoverClass : 'followingPageListB_o' }).stayElement({stayObject});
 * 
 */

(function($){
$.fn.extend({
	hoverElement : function(settings) {
		settings = $.extend({
			hoverClass : 'elmover'
		}, settings);
		this.hoverClass = settings.hoverClass;
		return this.each(function() {
			$(this).hover(
				function() { $(this).addClass(settings.hoverClass); },
				function() { $(this).removeClass(settings.hoverClass); }
			);
		});
	},
	stayElement : function(stayObj, settings) {
		settings = $.extend({
			stayClass : (this.hoverClass || 'elmstay')
		}, settings);
		this.stayClass = settings.stayClass;

		var stayIndex;
		var bodyClassList = document.body.className.split(' ');
		$.each(stayObj, function(objKey, objValue){
			var regObj = new RegExp(objKey);
			$.each(bodyClassList, function(arrayIndex, arrayValue){
				if(regObj.test(arrayValue)){
					stayIndex = objValue;
				}
			})
		})
		this.stayIndex = stayIndex;
		
		return this.each(function(index, elements) {
			if(index == stayIndex) {
				$(this).unbind();
				$(this).addClass(settings.stayClass);
			}
		});
	}
});
})($jq['base']);
